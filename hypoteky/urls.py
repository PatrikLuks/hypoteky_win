from django.contrib import admin
from django.urls import include, path

from drf_yasg import openapi
from drf_yasg.views import get_schema_view
from rest_framework import permissions

schema_view = get_schema_view(
    openapi.Info(
        title="Hypotéky API",
        default_version="v1",
        description="API dokumentace pro správu hypoték",
        contact=openapi.Contact(email="podpora@hypoteky.cz"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path("admin/", admin.site.urls),
    path("", include("klienti.urls")),
    path("api/", include("klienti.api_urls")),
    path(
        "swagger/",
        schema_view.with_ui("swagger", cache_timeout=0),
        name="schema-swagger-ui",
    ),
    path("redoc/", schema_view.with_ui("redoc", cache_timeout=0), name="schema-redoc"),
    path("account/", include("django.contrib.auth.urls")),
    # POZNÁMKA: two_factor není kompatibilní s Django 4.2 path() API v1.16.x
    # 2FA doporučujeme implementovat přes django-otp + custom views nebo upgrade two_factor
]
